    <h1 style="text-align: center; font-size: 150px;">Stratocaster</h1>
<div style="display:grid; grid-template-columns: repeat(2, 1fr);">
<p style="font-size: 25px; margin-left: 30px;">
    O modelo de guitarra mais famoso, a Fender Stratocaster, é conhecida por sua alta versatilidade, sendo muito escolhida por iniciantes,
    mas também muito usada por mestres do instrumento. Geralmente, quando pensamos em guitarras,  ela é a primeira a vir na nossa mente!
    <br> <br>
    A Fender Stratocaster foi criada por Leo Fender em 1954, e até hoje faz muito sucesso. Sendo muito versátil, pode ser usada em diversos estilos, como
    o country, reggae, rock, pop, soul, R&B (Rock & Blues), blues, jazz, punk rock e heavy metal.
    <br> <br>
    Em seus designs originais, a Stratocaster é equipada com três captadores Single-coil, uma ponte Tremolo para usar a famosa alavanca, que dá aquele "tempero"
    ao ser usada em alguns solos, um seletor com 5 posições para permitir uma grande variedade de timbres diferentes.
    <br> <br>
    Com a popularidade da Stratocaster, várias variações foram surgindo, como por exemplo as Stratocasters HSS, que possuem um captador Humbucker ao invés de um Single-coil na ponte
    da guitarra (essa ideia foi criada e popularizada após Eddie Van Halen colocar um Humbucker de outra guitarra na sua própria Stratocaster, fazendo uma "gambiarra". 
    Essa sua guitarra única ficou popularmente conhecida como "Frankenstrat", em referência ao personagem de terror Frankenstein)
</p>


