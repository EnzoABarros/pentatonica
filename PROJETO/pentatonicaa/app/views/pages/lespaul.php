    <h1 style="text-align: center; font-size: 150px;">Les Paul</h1>
<div style="display:grid; grid-template-columns: repeat(2, 1fr);">
<p style="font-size: 25px; margin-left: 30px;">
    O modelo de guitarra Les Paul, criado pela Gibson, é um dos mais icônicos do mundo, conhecido pelo seu som encorpado e sustain poderoso.
    Muito popular entre músicos de rock, blues e jazz, a Les Paul é frequentemente associada a solos marcantes e riffs pesados que definiram a história da música.
    <br> <br>
    A Gibson Les Paul foi desenvolvida em parceria com o guitarrista Les Paul no início dos anos 1950, buscando criar uma guitarra elétrica sólida, com mais
    controle sobre o sustain e a qualidade do som. Seu corpo robusto, geralmente feito de mogno com tampo de maple, entrega uma combinação de calor e brilho
    que a torna extremamente versátil.
    <br> <br>
    Originalmente, a Les Paul vinha equipada com dois captadores Humbucker, que ajudam a reduzir o ruído e oferecem um timbre mais cheio e pesado, ideal para solos poderosos e bases fortes.
    Seu design inclui também uma ponte Tune-o-matic fixa, que contribui para a estabilidade da afinação e para o sustain característico do instrumento.
    <br> <br>
    Com o passar dos anos, surgiram diversas variações da Les Paul, como a Les Paul Standard, Custom, e Studio, além de reedições vintage que buscam replicar o 
    som das primeiras unidades dos anos 50.
</p>


